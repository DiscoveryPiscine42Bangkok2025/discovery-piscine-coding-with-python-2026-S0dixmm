#!/usr/bin/env python

num1 = int(input("Give me a first number : "))
num2 = int(input("Give me a second number : "))
print("Thank you!")
print(f"{num1} + {num2} = ",num1+num2)
print(f"{num1} - {num2} = ",num1-num2)
print(f"{num1} / {num2} = ",int(num1/num2))
print(f"{num1} * {num2} = ",num1*num2)